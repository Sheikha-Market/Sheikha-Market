    // ═══ إنشاء معاملة دفع ═══
    createTransaction(data) {
        const txId = 'TXN-' + Date.now().toString(36).toUpperCase() + '-' + crypto.randomBytes(3).toString('hex').toUpperCase();
        const tx = {
            id: txId,
            بسم_الله: 'بسم الله الرحمن الرحيم',
            createdAt: new Date().toISOString(),
            amount: data.amount || 0,
            currency: data.currency || 'SAR',
            method: data.method || 'BANK_TRANSFER',
            status: 'pending',
            shariaCheck: 'passed',
            from: data.from || {},
            to: data.to || {},
            reference: data.reference || '',
            blockchainHash: crypto.createHash('sha256').update(txId + Date.now()).digest('hex').substring(0, 16)
        };
        this.db.transactions.push(tx);
        this.db.stats.total++;
        this.db.stats.volume += tx.amount;
        this._saveDB();
        return { success: true, transaction: tx };
    }


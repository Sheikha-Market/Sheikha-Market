    constructor() {
        this.name = 'منظومة شيخة — بيانات السكراب العالمية الشاملة';
        this.version = '1.0.0';
        this.data = SCRAP_GLOBAL_DATA;
    }

    getDashboard() {
        const types = Object.keys(this.data.types);
        return {
            بسم_الله: 'بسم الله الرحمن الرحيم',
            name: this.name,
            typesCount: types.length,
            types: types.map(k => ({ key: k, nameAr: this.data.types[k].nameAr })),
            historicalEras: this.data.history.eras.length,
            saudiSmelters: this.data.smelters.saudi.ironSteel.length,
            shariaEvidence: this.data.islamicFoundation.verses.length + this.data.islamicFoundation.hadith.length,
            contractIssues: this.data.contractlessTrading.currentReality.risks.length,
            smartContractFeatures: this.data.contractlessTrading.sheikhaSmartContract.features.length
        };
    }


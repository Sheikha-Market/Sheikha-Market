    getDashboard() {
        const totalTangibleTargets = this.programs.reduce((sum, p) => sum + p.tangibleTargets.length, 0);
        const totalImpactMetrics   = this.programs.reduce((sum, p) => sum + p.societalImpact.metrics.length, 0);
        const totalContributions   = this.programs.reduce((sum, p) => sum + p.sheikhaContribution.length, 0);

        return {
            sheikha: {
                name:    this.name,
                nameEn:  this.nameEn,
                pledge:  this.royalMandate.sheikhaCommitment.pledge,
            },
            royalMandate: {
                king:    this.royalMandate.king.nameAr,
                vision:  this.royalMandate.vision.nameAr,
                headline: this.royalMandate.vision.headline,
            },
            summary: {
                programs:             this.programs.length,
                tangibleTargets:      totalTangibleTargets,
                impactMetrics:        totalImpactMetrics,
                sheikhaContributions: totalContributions,
                sdgGoals:             this.impactFramework.sdgAlignment.length,
                roadmapPhases:        this.roadmap.phases.length,
            },
            programsSummary: this.programs.map(p => ({
                id:            p.id,
                sheikhaName:   p.sheikhaName,
                sheikhaLeader: p.sheikhaLeader,
                royalGoal:     p.royalGoal,
                pillar:        p.pillar,
                targets:       p.tangibleTargets.length,
            })),
            quranic_ref: { ref: 'الرعد:١١', text: 'إِنَّ اللَّهَ لَا يُغَيِّرُ مَا بِقَوْمٍ حَتَّىٰ يُغَيِّرُوا مَا بِأَنفُسِهِمْ' },
        };
    }

    getProgramById(id) {
        return this.programs.find(p => p.id === id || p.vision2030Id === id) || null;
    }

    getProgramsByLeader(leader) {
        return this.programs.filter(p => p.sheikhaLeader === leader);
    }

    getProgramsByPillar(pillar) {
        return this.programs.filter(p => p.pillar.startsWith(pillar));
    }

    getAllTangibleTargets() {
        return this.programs.flatMap(p =>
            p.tangibleTargets.map(t => ({ ...t, programId: p.id, programName: p.sheikhaName }))
        );
    }

    getAllImpactMetrics() {
        return this.programs.map(p => ({
            programId:   p.id,
            sheikhaName: p.sheikhaName,
            sheikhaLeader: p.sheikhaLeader,
            royalGoal:   p.royalGoal,
            impact:      p.societalImpact,
        }));
    }

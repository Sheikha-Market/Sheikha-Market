    /**
     * تقييم نية التشغيل لمنع الاستخدام الضار
     */
    evaluateIntentSafety(intent = '') {
        const normalizedIntent = String(intent || '')
            .toLowerCase()
            .trim();
        const blockedMarkers = [
            'attack',
            'harm',
            'abuse',
            'weaponize',
            'terror',
            'عدوان',
            'إيذاء',
            'قتل',
            'هجوم'
        ];

        const detected = blockedMarkers.filter(marker => normalizedIntent.includes(marker));
        if (detected.length > 0) {
            return {
                success: true,
                decision: 'blocked',
                reason: 'intent-conflicts-with-no-harm-policy',
                detectedMarkers: detected,
                timestamp: new Date().toISOString()
            };
        }

        return {
            success: true,
            decision: 'allowed',
            reason: 'intent-aligned-with-beneficial-use',
            detectedMarkers: [],
            timestamp: new Date().toISOString()
        };
    }


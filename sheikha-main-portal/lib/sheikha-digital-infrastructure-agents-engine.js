module.exports = {
    DIGITAL_INFRASTRUCTURE_AGENTS,

    /**
     * الحصول على جميع وكلاء البنية التحتية الرقمية
     */
    getAllDigitalInfrastructureAgents() {
        const agents = [];
        for (const [category, categoryAgents] of Object.entries(DIGITAL_INFRASTRUCTURE_AGENTS)) {
            for (const [key, agent] of Object.entries(categoryAgents)) {
                agents.push(agent);
            }
        }
        return agents;
    },

    /**
     * الحصول على وكيل حسب المعرف
     */
    getAgentById(agentId) {
        for (const category of Object.values(DIGITAL_INFRASTRUCTURE_AGENTS)) {
            for (const agent of Object.values(category)) {
                if (agent.id === agentId) {
                    return agent;
                }
            }
        }
        return null;
    },

    /**
     * الحصول على الوكلاء حسب الدور
     */
    getAgentsByRole(role) {
        return this.getAllDigitalInfrastructureAgents().filter(agent => agent.role === role);
    },

    /**
     * الحصول على الوكلاء حسب القسم
     */
    getAgentsByDepartment(department) {
        return this.getAllDigitalInfrastructureAgents().filter(
            agent => agent.department === department
        );
    },

    /**
     * الحصول على الوكلاء حسب المستوى
     */
    getAgentsByLevel(level) {
        return this.getAllDigitalInfrastructureAgents().filter(agent => agent.level === level);
    },

    /**
     * تفعيل وكيل حسب المعرف
     */
    activateAgent(agentId, config = {}) {
        const agent = this.getAgentById(agentId);
        if (!agent) {
            throw new Error(`الوكيل غير موجود: ${agentId}`);
        }

        return {
            ...agent,
            activated: true,
            activatedAt: new Date().toISOString(),
            config,
            status: 'ACTIVE',
            islamicBlessing: 'بسم الله الرحمن الرحيم - اللهم بارك'
        };
    },

    /**
     * تفعيل جميع الوكلاء في فئة معينة
     */
    activateCategory(category, config = {}) {
        if (!DIGITAL_INFRASTRUCTURE_AGENTS[category]) {
            throw new Error(`الفئة غير موجودة: ${category}`);
        }

        const agents = Object.values(DIGITAL_INFRASTRUCTURE_AGENTS[category]);
        return agents.map(agent => this.activateAgent(agent.id, config));
    },

    /**
     * الحصول على إحصائيات البنية التحتية
     */
    getInfrastructureStats() {
        const allAgents = this.getAllDigitalInfrastructureAgents();
        const stats = {
            totalAgents: allAgents.length,
            byRole: {},
            byDepartment: {},
            byLevel: {},
            categories: Object.keys(DIGITAL_INFRASTRUCTURE_AGENTS).length
        };

        allAgents.forEach(agent => {
            stats.byRole[agent.role] = (stats.byRole[agent.role] || 0) + 1;
            stats.byDepartment[agent.department] = (stats.byDepartment[agent.department] || 0) + 1;
            stats.byLevel[agent.level] = (stats.byLevel[agent.level] || 0) + 1;
        });

        return stats;
    },

    /**
     * توليد وكيل جديد ذاتياً (بمساعدة الذكاء الاصطناعي)
     */
    generateNewAgent(template, islamicPrinciples = []) {
        return {
            ...template,
            id: `agent-generated-${Date.now()}`,
            generated: true,
            generatedAt: new Date().toISOString(),
            islamicPrinciples: ['توليد مبارك بإذن الله', 'العمل وفق الشريعة', ...islamicPrinciples],
            status: 'GENERATED'
        };
    }
};

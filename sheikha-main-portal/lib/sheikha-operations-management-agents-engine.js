module.exports = {
    OPERATIONS_AGENTS,

    /**
     * الحصول على جميع وكلاء العمليات والإدارة
     */
    getAllOperationsAgents() {
        const agents = [];
        for (const [categoryKey, category] of Object.entries(OPERATIONS_AGENTS)) {
            if (category && typeof category === 'object') {
                for (const [agentKey, agent] of Object.entries(category)) {
                    if (agent && agent.id) {
                        agents.push(agent);
                    }
                }
            }
        }
        return agents;
    },

    /**
     * الحصول على وكيل بواسطة ID
     */
    getAgentById(agentId) {
        for (const category of Object.values(OPERATIONS_AGENTS)) {
            for (const agent of Object.values(category)) {
                if (agent && agent.id === agentId) {
                    return agent;
                }
            }
        }
        return null;
    },

    /**
     * الحصول على وكلاء حسب الدور
     */
    getAgentsByRole(role) {
        return this.getAllOperationsAgents().filter(agent => agent.role === role);
    },

    /**
     * الحصول على وكلاء حسب القسم
     */
    getAgentsByDepartment(department) {
        return this.getAllOperationsAgents().filter(agent => agent.department === department);
    },

    /**
     * الحصول على إحصائيات الوكلاء
     */
    getAgentsStatistics() {
        const allAgents = this.getAllOperationsAgents();
        const byRole = {};
        const byDepartment = {};
        const byPriority = {};

        allAgents.forEach(agent => {
            // حسب الدور
            byRole[agent.role] = (byRole[agent.role] || 0) + 1;

            // حسب القسم
            if (agent.department) {
                byDepartment[agent.department] = (byDepartment[agent.department] || 0) + 1;
            }

            // حسب الأولوية
            const priority = agent.priority || 'غير محدد';
            byPriority[priority] = (byPriority[priority] || 0) + 1;
        });

        return {
            total: allAgents.length,
            byRole,
            byDepartment,
            byPriority,
            categories: Object.keys(OPERATIONS_AGENTS).length
        };
    },

    /**
     * تفعيل وكيل
     */
    activateAgent(agentId, config = {}) {
        const agent = this.getAgentById(agentId);
        if (!agent) return null;

        return {
            ...agent,
            status: 'active',
            activatedAt: new Date().toISOString(),
            config: config
        };
    },

    /**
     * تفعيل عدة وكلاء
     */
    activateMultipleAgents(agentIds, config = {}) {
        return agentIds.map(id => this.activateAgent(id, config)).filter(Boolean);
    },

    /**
     * تفعيل جميع وكلاء دور معين
     */
    activateAllAgentsByRole(role, config = {}) {
        return this.getAgentsByRole(role).map(agent =>
            this.activateAgent(agent.id, { ...config, role })
        );
    },

    /**
     * البحث عن وكلاء
     */
    searchAgents(query) {
        const lowerQuery = query.toLowerCase();
        return this.getAllOperationsAgents().filter(
            agent =>
                agent.nameAr?.toLowerCase().includes(lowerQuery) ||
                agent.nameEn?.toLowerCase().includes(lowerQuery) ||
                agent.department?.toLowerCase().includes(lowerQuery) ||
                agent.role?.toLowerCase().includes(lowerQuery)
        );
    }
};

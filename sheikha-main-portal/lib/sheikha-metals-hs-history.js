class SheikhaMetalsHSHistory {
    constructor() {
        this.name = 'منظومة شيخة — أكواد HS وتاريخ المعادن';
        this.version = '1.0.0';
        this.data = METALS_HS_HISTORY;
    }

    getDashboard() {
        const ironSections = this.data.iron.sections.length;
        const alSections = this.data.aluminum.sections.length;
        const cuSections = this.data.copper.sections.length;
        const pmSections = this.data.preciousMetals.sections.length;
        let totalHS = 0;
        [this.data.iron, this.data.aluminum, this.data.copper, this.data.preciousMetals].forEach(m => {
            m.sections.forEach(s => { totalHS += s.items.length; });
        });
        return {
            بسم_الله: 'بسم الله الرحمن الرحيم',
            name: this.name,
            hsCodes: { total: totalHS, iron: ironSections + ' قسم', aluminum: alSections + ' قسم', copper: cuSections + ' قسم', precious: pmSections + ' قسم' },
            history: { eras: this.data.history.eras.length, quranMentions: this.data.history.islamicFoundation.metalsMentionedInQuran.length }
        };
    }

    getHSCodes(metal) {
        const m = this.data[metal];
        if (!m) return null;
        return { بسم_الله: 'بسم الله الرحمن الرحيم', المعدن: m.nameAr, الفصول: m.hsChapters || m.hsChapter, الأقسام: m.sections };
    }

    getHSByStage(metal, stage) {
        const m = this.data[metal];
        if (!m) return null;
        const filtered = m.sections.filter(s => s.stage && s.stage.includes(stage));
        return { المعدن: m.nameAr, المرحلة: stage, الأقسام: filtered };
    }

    getHistory() {
        return { بسم_الله: 'بسم الله الرحمن الرحيم', ...this.data.history };
    }

    getHistoryEra(index) {
        const era = this.data.history.eras[index];
        if (!era) return null;
        return { بسم_الله: 'بسم الله الرحمن الرحيم', ...era };
    }

    getQuranMetals() {
        return { بسم_الله: 'بسم الله الرحمن الرحيم', ...this.data.history.islamicFoundation };
    }

    getHSSystem() {
        return { بسم_الله: 'بسم الله الرحمن الرحيم', ...this.data.hsCodeSystem };
    }

    searchHS(query) {
        const results = [];
        const q = query.toLowerCase();
        [this.data.iron, this.data.aluminum, this.data.copper, this.data.preciousMetals].forEach(m => {
            m.sections.forEach(s => {
                s.items.forEach(item => {
                    if (item.hs.includes(q) || (item.nameAr && item.nameAr.includes(query)) || (item.nameEn && item.nameEn.toLowerCase().includes(q)) || (item.desc && item.desc.includes(query))) {
                        results.push({ metal: m.nameAr, section: s.sectionAr, ...item });
                    }
                });
            });
        });
        return { query, results, count: results.length };
    }

    getMetalKeys() {
        return ['iron', 'aluminum', 'copper', 'preciousMetals'];
    }
}

module.exports = SheikhaMetalsHSHistory;

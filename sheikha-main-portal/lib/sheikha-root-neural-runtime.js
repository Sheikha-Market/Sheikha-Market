/**
 * نبضة الجذر — تُشغّل كل الشبكات بشكل موحَّد
 * @param {object} input — { type, context, data }
 * @returns {object}
 */
function pulse(input = {}) {
    if (!_ready) init();
    _pulseCount++;

    const { type = 'general', context = '', data = {} } = input;
    const ts = new Date().toISOString();

    const result = {
        id:          `root_pulse_${Date.now()}_${_pulseCount}`,
        tawheed:     TAWHEED,
        timestamp:   ts,
        type,
        rootCells:   _activeCells.size,
        networks:    {},
    };

    // تشغيل الشبكة الشاملة
    if (_networks.universal && typeof _networks.universal.infer === 'function') {
        try {
            result.networks.universal = _networks.universal.infer({ type, context, data });
        } catch (_) { result.networks.universal = { status: 'error' }; }
    }

    // تشغيل خلايا الشبكة الجذرية
    if (_networks.neuralCells && typeof _networks.neuralCells.process === 'function') {
        try {
            result.networks.neuralCells = _networks.neuralCells.process({ type, data, context });
        } catch (_) { result.networks.neuralCells = { status: 'error' }; }
    }

    // تشغيل محرك التوحيد
    if (_networks.unityEngine && typeof _networks.unityEngine.pulse === 'function') {
        try {
            result.networks.unityEngine = _networks.unityEngine.pulse({ type, data, context });
        } catch (_) { result.networks.unityEngine = { status: 'error' }; }
    }

    // تحديث عداد الإطلاق لكل خلية جذرية
    for (const [, cell] of _activeCells) {
        cell.fireCount++;
    }

    return result;
}


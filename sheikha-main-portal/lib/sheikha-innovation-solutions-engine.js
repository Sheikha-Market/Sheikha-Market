    getDashboard() {
        const allPowers = [this.researchPower, this.explorationPower, this.alternativesPower,
            this.scrapInnovation, this.miningResources, this.managementPower, this.innovativeSolutions];
        const totalBgOps = allPowers.reduce((s,p) => s + (p.backgroundOps||[]).length, 0);
        return {
            بسم_الله: 'بسم الله الرحمن الرحيم',
            name: this.name, version: this.version, owner: this.owner,
            totalModules: 7, totalBackgroundOps: totalBgOps, allRunning: true,
            modules: allPowers.map(p => ({ id: p.id, name: p.nameAr, icon: p.icon, status: p.status, bgOps: (p.backgroundOps||[]).length })),
            islamicFoundation: { verses: this.islamicFoundation.verses.length, hadith: this.islamicFoundation.hadith.length },
            database: { queries: this.db.queries.length, solutions: this.db.solutions.length, lastUpdated: this.db.lastUpdated }
        };
    }


    // بناء فهرس البحث
    // ══════════════════════════════════════════════════════════
    _buildSearchIndex() {
        const index = [];
        this.sections.forEach((sec) => {
            index.push({ id: sec.id, type: 'section', text: sec.title + ' ' + sec.summary + ' ' + (sec.islamicPrinciple || ''), ref: sec.id });
            if (sec.steps) {
                sec.steps.forEach((s) => {
                    index.push({ id: sec.id + '_step_' + s.step, type: 'step', text: s.title + ' ' + s.desc, ref: sec.id });
                });
            }
        });
        this.faq.forEach((f, i) => {
            index.push({ id: 'faq_' + i, type: 'faq', text: f.q + ' ' + f.a, ref: 'faq' });
        });
        return index;
    }

    // ══════════════════════════════════════════════════════════
    // البحث الفوري
    // ══════════════════════════════════════════════════════════
    search(query = '') {
        const q = query.trim().toLowerCase();
        if (!q || q.length < 2) return { sections: [], faq: [], total: 0 };

        const matchedSectionIds = new Set();
        const matchedFaqIndices = [];

        this.searchIndex.forEach((item) => {
            if (item.text.toLowerCase().includes(q) || item.text.includes(query.trim())) {
                if (item.type === 'faq') {
                    const idx = parseInt(item.id.replace('faq_', ''));
                    if (!isNaN(idx)) matchedFaqIndices.push(idx);
                } else {
                    matchedSectionIds.add(item.ref);
                }
            }
        });

        const sections = this.sections.filter((s) => matchedSectionIds.has(s.id));
        const faq = matchedFaqIndices.map((i) => this.faq[i]).filter(Boolean);

        return { sections, faq, total: sections.length + faq.length, query };
    }

    // ══════════════════════════════════════════════════════════
    // قسم بعينه
    // ══════════════════════════════════════════════════════════
    getSection(sectionId) {
        return this.sections.find((s) => s.id === sectionId) || null;
    }

    // ══════════════════════════════════════════════════════════
    // ملخص الدليل
    // ══════════════════════════════════════════════════════════
    getSummary() {
        const categories = {};
        this.sections.forEach((s) => {
            if (!categories[s.category]) categories[s.category] = [];
            categories[s.category].push({ id: s.id, title: s.title, icon: s.icon });
        });
        return {
            name: this.name,
            version: this.version,
            quranRef: this.quranRef,
            totalSections: this.sections.length,
            totalFAQ: this.faq.length,
            totalSteps: this.sections.reduce((acc, s) => acc + (s.steps ? s.steps.length : 0), 0),
            categories,
            timestamp: new Date().toISOString()
        };
    }

class GovernanceDecision {
    constructor(data = {}) {
        this.id          = data.id          || uuid();
        this.type        = data.type        || 'general'; // trade | org | market | dispute
        this.entityId    = data.entityId    || null;      // معرّف المنظمة أو السوق
        this.entityType  = data.entityType  || null;      // 'org' | 'market' | 'transaction'
        this.title       = data.title       || '';
        this.description = data.description || '';

        // ─── النتيجة ───────────────────────────────────────────────────────────
        this.verdict     = data.verdict     || 'pending'; // compliant | non-compliant | needs-review
        this.shariaRef   = data.shariaRef   || [];        // مراجع شرعية داعمة
        this.reviewedBy  = data.reviewedBy  || null;
        this.reviewedAt  = data.reviewedAt  || null;

        // ─── المراحل ───────────────────────────────────────────────────────────
        this.stages = data.stages || [
            { id: 'charter-check',   nameAr: 'فحص الميثاق',      status: 'pending' },
            { id: 'fiqh-review',     nameAr: 'المراجعة الفقهية',   status: 'pending' },
            { id: 'council-approval',nameAr: 'موافقة المجلس',      status: 'pending' },
            { id: 'execution',       nameAr: 'التنفيذ',             status: 'pending' }
        ];

        this.createdAt = data.createdAt || new Date().toISOString();
        this.updatedAt = data.updatedAt || new Date().toISOString();
    }

    save() {
        this.updatedAt = new Date().toISOString();
        let decisions = database.read('governance') || [];
        if (!Array.isArray(decisions)) decisions = [];
        const index = decisions.findIndex(d => d.id === this.id);
        if (index >= 0) decisions[index] = this;
        else decisions.push(this);
        database.write('governance', decisions);
        return this;
    }

    static findById(id) {
        const decisions = database.read('governance') || [];
        const data = decisions.find(d => d.id === id);
        return data ? new GovernanceDecision(data) : null;
    }

    static find(query = {}) {
        let decisions = database.read('governance') || [];
        if (!Array.isArray(decisions)) decisions = [];
        if (Object.keys(query).length === 0) return decisions.map(d => new GovernanceDecision(d));
        return decisions
            .filter(d => Object.entries(query).every(([k, v]) => d[k] === v))
            .map(d => new GovernanceDecision(d));
    }

    static create(data) {
        return new GovernanceDecision(data).save();
    }
}


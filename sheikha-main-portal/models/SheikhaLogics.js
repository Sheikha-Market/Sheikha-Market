/**
 * الحصول على منطق بمعرّفه (يبحث في ALL_LOGICS + MASTER_LOGIC)
 */
function getLogicById(id) {
    if (id === LOGIC_TYPES.MASTER) return MASTER_LOGIC;
    return ALL_LOGICS.find(l => l.id === id) || null;
}

/**
 * الحصول على جميع المنطق أو فلترة حسب الحالة أو الفئة
 *   category: 'core' → السباعي فقط
 *   category: 'extended' → الموسّع فقط
 *   category: 'all' أو null → الكل (بدون المنطق الأعلى)
 *   category: 'master' → المنطق الأعلى فقط
 */
function getAllLogics(status = null, category = null) {
    let pool;

    if (category === 'core') {
        pool = SEVEN_LOGICS;
    } else if (category === 'extended') {
        pool = EXTENDED_LOGICS;
    } else if (category === 'master') {
        pool = [MASTER_LOGIC];
    } else {
        pool = ALL_LOGICS;
    }

    if (status) return pool.filter(l => l.status === status);
    return pool;
}

/**
 * الحصول على تطبيقات منطق معيّن في نطاق معيّن
 */
function getLogicScope(logicId, scopeId) {
    const logic = getLogicById(logicId);
    if (!logic) return null;
    return logic.scopes[scopeId] || null;
}

/**
 * الحصول على جميع تطبيقات نطاق معيّن عبر كل المنطق
 */
function getScopeView(scopeId) {
    if (!Object.values(SCOPE_TYPES).includes(scopeId)) return null;

    return ALL_LOGICS.map(logic => ({
        logicId:  logic.id,
        nameAr:   logic.nameAr,
        nameEn:   logic.nameEn,
        icon:     logic.icon,
        status:   logic.status,
        category: logic.category || 'core',
        scope:    logic.scopes[scopeId] || null
    }));
}

